// WriteCases writes suggested BVT cases to disk under test/distributed/cases/.
// Returns the list of written file paths (relative to repoRoot).
// WriteCases writes suggested test cases to disk.
// BVT/PITR/Snapshot → test/distributed/cases/{category}/
// Stability/Chaos/BigData → output/{type}/{category}/ (for manual copy to nightly repo)
func WriteCases(repoRoot string, cases []types.SuggestedCase) ([]string, error) {
	if len(cases) == 0 {
		return nil, nil
	}

	var written []string
	for _, sc := range cases {
		if sc.Category == "" || sc.Filename == "" || sc.Content == "" {
			continue
		}

		var dir, relPath string
		switch sc.Type {
		case types.TestBVT, types.TestPITR, types.TestSnapshot:
			// These go directly into the matrixone repo
			dir = filepath.Join(repoRoot, "test", "distributed", "cases", sc.Category)
			relPath = filepath.Join("test", "distributed", "cases", sc.Category, sc.Filename)
		case types.TestStability, types.TestChaos, types.TestBigData:
			// These are staged in output/ for manual copy to mo-nightly-regression
			dir = filepath.Join(repoRoot, "output", string(sc.Type), sc.Category)
			relPath = filepath.Join("output", string(sc.Type), sc.Category, sc.Filename)
		default:
			continue
		}

		if err := os.MkdirAll(dir, 0o755); err != nil {
			return written, moerr.NewInternalErrorNoCtxf("mkdir %s: %v", dir, err)
		}

		fpath := filepath.Join(dir, sc.Filename)

		// Don't overwrite existing files
		if _, err := os.Stat(fpath); err == nil {
			fmt.Fprintf(os.Stderr, "skip: %s already exists\n", relPath)
			continue
		}

		if err := os.WriteFile(fpath, []byte(sc.Content), 0o644); err != nil {
			return written, moerr.NewInternalErrorNoCtxf("write %s: %v", relPath, err)
		}
		written = append(written, relPath)
	}
	return written, nil
}

// CreatePR creates a new branch, commits the written case files, and opens a PR.
// Returns the PR URL.
func CreatePR(repoRoot string, repo string, prNumber int, files []string) (string, error) {
	if len(files) == 0 {
		return "", moerr.NewInternalErrorNoCtx("no files to commit")
	}

	branch := "testinfra/pr-" + strconv.Itoa(prNumber) + "-cases"
	title := fmt.Sprintf("test: add BVT cases for PR #%d", prNumber)
	body := fmt.Sprintf("Auto-generated BVT test cases for PR #%d.\n\nGenerated by `mo-testplan`.", prNumber)

	type step struct {
		name string
		args []string
	}

	steps := []step{
		{"git", []string{"-C", repoRoot, "checkout", "-b", branch}},
	}
	for _, f := range files {
		steps = append(steps, step{"git", []string{"-C", repoRoot, "add", f}})
	}
	steps = append(steps,
		step{"git", []string{"-C", repoRoot, "commit", "-m", title}},
		step{"git", []string{"-C", repoRoot, "push", "origin", branch}},
		step{"gh", []string{"pr", "create", "--repo", repo, "--head", branch,
			"--title", title, "--body", body}},
	)

	for _, s := range steps {
		cmd := exec.Command(s.name, s.args...)
		cmd.Stderr = os.Stderr
		out, err := cmd.Output()
		if err != nil {
			return "", moerr.NewInternalErrorNoCtxf("%s %v failed: %v", s.name, s.args, err)
		}
		// The last command (gh pr create) outputs the PR URL
		if s.name == "gh" {
			return string(out), nil
		}
	}
	return "", nil
}
