package colexec

import (
	"bytes"
	"encoding/binary"
	"sync"

	"github.com/matrixorigin/matrixone/pkg/container/types"
)

const (
	TxnWorkspaceUnCommitType = 1
)

var (
	// TxnWorkspaceSegment includes a dedicated header which indicates it is a workspace segment.
	TxnWorkspaceSegment = types.Segmentid([16]byte{
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0xFF,
	})
)

type CnSegmentMap struct {
	sync.Mutex
	// tag whether a segment is generated by this txn
	mp map[string]int32
}

func IsDeletionOnTxnUnCommit(
	segmentMap map[string]int32,
	segId *types.Segmentid,
	tableId uint64,
	txnID []byte,
) bool {
	// raw row id generated by txn.
	// WriteBatch()
	if segId.EQ(&TxnWorkspaceSegment) {
		return true
	}

	// check if it is a persisted segment generated by a workspace.
	if segmentMap != nil {
		return segmentMap[segmentKey(txnID, tableId, segId)] == TxnWorkspaceUnCommitType
	} else {
		ss := Get()
		if ss == nil {
			return false
		}

		return ss.GetCnSegmentType(segId, tableId, txnID) == TxnWorkspaceUnCommitType
	}
}

// IsDeletionOnTxnUnCommitPersisted check if the segId
// is an UnCommit and flushed segment in the workspace.
// if the input map is nil, this check will be done by the colexec.Get.
func IsDeletionOnTxnUnCommitPersisted(
	segmentMap map[string]int32,
	segId *types.Segmentid,
	tableId uint64,
	txnID []byte,
) bool {
	if IsDeletionOnTxnUnCommit(segmentMap, segId, tableId, txnID) {
		return !segId.EQ(&TxnWorkspaceSegment)
	}
	return false
}

// IsDeletionOnTxnUnCommitInMem check if the segId
// is an UnCommit inmem segment in the workspace.
// if the input map is nil, this check will be done by the colexec.Get.
func IsDeletionOnTxnUnCommitInMem(
	segmentMap map[string]int32,
	segId *types.Segmentid,
	tableId uint64,
	txnID []byte,
) bool {
	if IsDeletionOnTxnUnCommit(segmentMap, segId, tableId, txnID) {
		return segId.EQ(&TxnWorkspaceSegment)
	}
	return false
}

func RecordTxnUnCommitSegment(txnID []byte, tableId uint64, segId *types.Segmentid) {
	Get().PutCnSegment(txnID, tableId, segId, TxnWorkspaceUnCommitType)
}

func segmentKey(txnID []byte, tableId uint64, segId *types.Segmentid) string {
	if len(txnID) > maxTxnIDLen {
		txnID = txnID[:maxTxnIDLen]
	}
	key := make([]byte, 2+len(txnID)+8+types.SegmentidSize)
	binary.BigEndian.PutUint16(key[:2], uint16(len(txnID)))
	copy(key[2:], txnID)
	binary.BigEndian.PutUint64(key[2+len(txnID):], tableId)
	copy(key[2+len(txnID)+8:], segId[:])
	return string(key)
}

func segmentKeyPrefix(txnID []byte) []byte {
	if len(txnID) > maxTxnIDLen {
		txnID = txnID[:maxTxnIDLen]
	}
	prefix := make([]byte, 2+len(txnID))
	binary.BigEndian.PutUint16(prefix[:2], uint16(len(txnID)))
	copy(prefix[2:], txnID)
	return prefix
}

func segmentKeyHasTxn(key string, txnID []byte) bool {
	prefix := segmentKeyPrefix(txnID)
	if len(key) < len(prefix) {
		return false
	}
	return bytes.HasPrefix([]byte(key), prefix)
}

const maxTxnIDLen = int(^uint16(0))
