// functions to make a scalar vector for test.
var (
	MakeScalarNull = func(t testing.TB, typ types.T, length int, mp *mpool.MPool) *vector.Vector {
		return vector.NewConstNull(typ.ToType(), length, NewProc(t).Mp())
	}
	MakeScalarInt64 = func(v int64, length int, mp *mpool.MPool) *vector.Vector {
		return makeScalar(v, length, int64Type, mp)
	}

	MakeScalarVarchar = func(value string, length int, mp *mpool.MPool) *vector.Vector {
		return makeScalarString(value, length, varcharType, mp)
	}
)

func makeVector[T types.FixedSizeT](values []T, nsp []uint64, typ types.Type, mp *mpool.MPool) *vector.Vector {
	vec := vector.NewVec(typ)
	err := vector.AppendFixedList(vec, values, nil, mp)
	vec.SetNulls(nulls.Build(len(values), nsp...))

	if err != nil {
		panic(err)
	}
	return vec
}

func makeStringVector(values []string, nsp []uint64, typ types.Type, mp *mpool.MPool) *vector.Vector {
	var err error

	vec := vector.NewVec(typ)
	if nsp == nil {
		err = vector.AppendStringList(vec, values, nil, mp)
	} else {
		err = vector.AppendStringList(vec, values, nil, mp)
		vec.SetNulls(nulls.Build(len(values), nsp...))
	}
	if err != nil {
		panic(err)
	}

	return vec
}

func makeJsonVector(values []string, nsp []uint64, mp *mpool.MPool) *vector.Vector {
	var err error

	newVals := make([][]byte, len(values))
	for i := range values {
		json, err := types.ParseStringToByteJson(values[i])
		if err != nil {
			panic(err)
		}
		newVals[i], err = types.EncodeJson(json)
		if err != nil {
			panic(err)
		}
	}

	vec := vector.NewVec(jsonType)
	if nsp == nil {
		err = vector.AppendBytesList(vec, newVals, nil, mp)
	} else {
		err = vector.AppendBytesList(vec, newVals, nil, mp)
		vec.SetNulls(nulls.Build(len(values), nsp...))
	}
	if err != nil {
		panic(err)
	}

	return vec
}

