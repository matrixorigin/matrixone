func makeScalar[T types.FixedSizeT](value T, length int, typ types.Type, mp *mpool.MPool) *vector.Vector {
	v, err := vector.NewConstFixed(typ, value, length, mp)
	if err != nil {
		panic(err)
	}
	return v
}

func makeScalarString(value string, length int, typ types.Type, mp *mpool.MPool) *vector.Vector {
	v, err := vector.NewConstBytes(typ, []byte(value), length, mp)
	if err != nil {
		panic(err)
	}
	return v
}

func MakeVarlenaVector(data [][]byte, nulls []uint64, typ types.Type, mp *mpool.MPool) *vector.Vector {
	vec := vector.NewVec(typ)
	for _, d := range data {
		if d != nil {
			vector.AppendBytes(vec, d, false, mp)
		} else {
			vector.AppendBytes(vec, nil, true, mp)
		}
	}

	if len(nulls) > 0 {
		vecNsp := vec.GetNulls()
		for _, row := range nulls {
			vecNsp.Add(row)
		}
	}

	return vec
}
