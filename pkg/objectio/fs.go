// Offline fs kinds, matching the --local / --s3 / --local2 tool flags. They
// select the on-disk format/layout of an offline data dir:
//   - local:  LocalFS, legacy CRC32-framed format
//   - local2: LocalFS, raw (checksum-free) DISK-V2 format
//   - s3:     S3FS over a local "disk" endpoint, raw (S3FS-on-disk) format
const (
	OfflineKindLocal  = "local"
	OfflineKindLocal2 = "local2"
	OfflineKindS3     = "s3"
)

// OfflineKindStrict resolves exactly one of the --local/--s3/--local2 flags.
// It errors when none, or more than one, is set: the formats are mutually
// incompatible and cannot be auto-detected, so the operator must state which
// one the data dir is in rather than rely on a silent (possibly wrong) default.
func OfflineKindStrict(local, s3, local2 bool) (string, error) {
	kind := ""
	n := 0
	if local {
		kind = OfflineKindLocal
		n++
	}
	if s3 {
		kind = OfflineKindS3
		n++
	}
	if local2 {
		kind = OfflineKindLocal2
		n++
	}
	switch {
	case n == 0:
		return "", moerr.NewInvalidInputNoCtx("no format specified; use one of --local, --s3, --local2")
	case n > 1:
		return "", moerr.NewInvalidInputNoCtx("conflicting formats specified; choose one of --local, --s3, --local2")
	}
	return kind, nil
}

func TmpNewFileservice(ctx context.Context, dir string) fileservice.FileService {
	return tmpNewFileservice(ctx, defines.LocalFileServiceName, dir)
}

func TmpNewSharedFileservice(ctx context.Context, dir string) fileservice.FileService {
	return tmpNewFileservice(ctx, defines.SharedFileServiceName, dir)
}

func tmpNewFileservice(ctx context.Context, kind string, dir string) fileservice.FileService {
	// Fallback for tests / standalone TAE that build options without injecting a
	// .toml-configured fileservice. A real cluster always injects its configured
	// fileservices, so this should never fire in production; if it does, it means
	// an injection was missed and DISK-V2 (raw) format may be written where DISK
	// was expected — warn loudly rather than switch formats silently.
	logutil.Warnf(
		"objectio: tmpNewFileservice DISK-V2 fallback used (name=%s dir=%s); "+
			"a .toml-configured fileservice should be injected instead", kind, dir)
	c := fileservice.Config{
		Name: kind,
		// DISK-V2 (raw) to match the default cluster data format. This is only a
		// fallback for tests / standalone TAE that build options without a
		// configured fileservice; a real cluster always injects its .toml-driven
		// fileservices (tnservice/factory.go), so this path is never hit there.
		Backend: "DISK-V2",
		DataDir: dir,
		Cache:   fileservice.DisabledCacheConfig,
	}
	service, err := fileservice.NewFileService(ctx, c, nil)
	if err != nil {
		err = moerr.NewInternalErrorNoCtx(fmt.Sprintf("NewFileService failed: %s", err.Error()))
		panic(any(err))
	}
	return service
}

// NewOfflineFS creates a file service for reading an offline data directory in
// the on-disk format selected by kind (see the OfflineKind* constants):
//   - s3:     data generated by the S3-backed fileservice (raw, S3FS-on-disk)
//   - local2: data generated by the DISK-V2 LocalFS (raw)
//   - local:  data generated by the legacy DISK LocalFS (CRC32-framed)
func NewOfflineFS(
	ctx context.Context,
	dir string,
	kind string,
) (fs fileservice.FileService, err error) {
	switch strings.ToLower(kind) {
	case OfflineKindS3:
		cfg := fileservice.ObjectStorageArguments{
			Name:     defines.SharedFileServiceName,
			Endpoint: "DISK",
			Bucket:   dir,
		}
		fs, err = fileservice.NewS3FS(ctx, cfg, fileservice.DisabledCacheConfig, nil, false, true)
		return

	case OfflineKindLocal2:
		cfg := fileservice.Config{
			Name:    defines.LocalFileServiceName,
			Backend: "DISK-V2",
			DataDir: dir,
			Cache:   fileservice.DisabledCacheConfig,
		}
		fs, err = fileservice.NewFileService(ctx, cfg, nil)
		return

	case OfflineKindLocal:
		cfg := fileservice.Config{
			Name:    defines.LocalFileServiceName,
			Backend: "DISK",
			DataDir: dir,
			Cache:   fileservice.DisabledCacheConfig,
		}
		fs, err = fileservice.NewFileService(ctx, cfg, nil)
		return

	default:
		// DISK and DISK-V2 are mutually incompatible and cannot be
		// auto-detected, so a typo must fail loudly rather than silently
		// default to one format and misread the other.
		return nil, moerr.NewInvalidInputNoCtxf(
			"unknown offline fs kind %q: expected one of %q, %q, %q",
			kind, OfflineKindLocal, OfflineKindLocal2, OfflineKindS3)
	}
}
