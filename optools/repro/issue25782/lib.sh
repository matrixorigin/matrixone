#!/usr/bin/env bash
# Shared safety and lifecycle helpers for the MatrixOne issue-25782 harness.
set -euo pipefail

if [[ -z "${HARNESS_DIR+x}" ]]; then
    HARNESS_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd -P)"
fi
if [[ -z "${REPO_ROOT+x}" ]]; then
    REPO_ROOT="$(cd -- "${HARNESS_DIR}/../../.." && pwd -P)"
fi
readonly HARNESS_DIR REPO_ROOT

die() {
    printf 'issue25782: %s\n' "$*" >&2
    return 1
}

log_msg() {
    local msg="$*"
    if [[ -n "${MO_25782_LOG_FILE:-}" ]]; then
        printf '%s %s\n' "$(date -Is)" "$msg" >>"${MO_25782_LOG_FILE}" || true
    fi
    printf '%s\n' "$msg" >&2
}

runtime_path() {
    local requested="${1:-${MO_25782_RUNTIME:-}}"
    [[ -n "${requested}" ]] || die "runtime path is required (use --runtime or MO_25782_RUNTIME)"
    [[ "${requested}" = /* ]] || die "runtime path must be absolute: ${requested}"
    local parent base canonical_parent
    parent="$(dirname -- "${requested}")"
    base="$(basename -- "${requested}")"
    [[ -n "${base}" && "${base}" != "." && "${base}" != ".." ]] || die "invalid runtime path"
    [[ -d "${parent}" ]] || die "runtime parent does not exist: ${parent}"
    canonical_parent="$(realpath -e -- "${parent}")" || die "cannot resolve runtime parent"
    [[ "${canonical_parent}" = "${parent}" ]] || die "runtime parent contains symlink: ${parent}"
    printf '%s/%s\n' "${canonical_parent}" "${base}"
}

assert_runtime_private() {
    local root="$1" mode owner uid
    [[ -d "${root}" && ! -L "${root}" ]] || die "runtime root is not a directory or is a symlink: ${root}"
    [[ "$(realpath -e -- "${root}")" = "${root}" ]] || die "runtime root path is not canonical: ${root}"
    mode="$(stat -c '%a' -- "${root}")"
    [[ "${mode}" = 700 ]] || die "runtime root mode is ${mode}; expected 0700"
    uid="$(id -u)"
    owner="$(stat -c '%u' -- "${root}")"
    [[ "${owner}" = "${uid}" ]] || die "runtime root is not owned by current user"
}

load_runtime() {
    local requested="${1:-${MO_25782_RUNTIME:-}}"
    local root
    root="$(runtime_path "${requested}")"
    assert_runtime_private "${root}"
    [[ -f "${root}/harness.env" && ! -L "${root}/harness.env" ]] || die "missing harness.env under ${root}"
    # harness.env is generated by prepare.sh and contains shell assignments only.
    # shellcheck disable=SC1090
    source "${root}/harness.env"
    [[ "${MO_25782_RUNTIME:-}" = "${root}" ]] || die "harness.env runtime mismatch"
    export MO_25782_RUNTIME="${root}"
    export MO_25782_LOG_FILE="${root}/harness.log"
    mkdir -p -- "${root}/manifest" "${root}/logs"
    chmod 0700 -- "${root}/manifest" "${root}/logs"
}

uuid_new() {
    local u
    if command -v uuidgen >/dev/null 2>&1; then
        u="$(uuidgen)"
    else
        u="$(cat /proc/sys/kernel/random/uuid)"
    fi
    [[ "${u}" =~ ^[0-9a-fA-F-]{36}$ ]] || die "invalid generated uuid"
    printf '%s\n' "${u,,}"
}

port_in_use() {
    local p="$1"
    if command -v ss >/dev/null 2>&1; then
        ss -H -ltn 2>/dev/null | awk -v p=":${p}" '$4 ~ p "$" {found=1} END {exit !found}' && return 0
        ss -H -lun 2>/dev/null | awk -v p=":${p}" '$4 ~ p "$" {found=1} END {exit !found}' && return 0
        return 1
    fi
    if command -v lsof >/dev/null 2>&1; then
        lsof -nP -iTCP:"${p}" -sTCP:LISTEN >/dev/null 2>&1 && return 0
        lsof -nP -iUDP:"${p}" >/dev/null 2>&1 && return 0
        return 1
    fi
    return 2
}

precheck_ports() {
    local ports="${1:-${PORTS_ALL:-}}" p rc
    [[ -n "${ports}" ]] || die "no ports configured"
    for p in ${ports}; do
        if port_in_use "${p}"; then
            rc=0
        else
            rc=$?
        fi
        case "${rc}" in
            0) die "port ${p} is already listening; refusing to kill or reuse occupant" ;;
            1) : ;;
            *) die "cannot verify port ${p}; install ss or lsof" ;;
        esac
    done
}

mem_available_bytes() {
    awk '/^MemAvailable:/ {print $2 * 1024; found=1} END {if (!found) exit 1}' /proc/meminfo
}

memory_max_bytes() {
    case "$1" in
        log)  printf '%s\n' $((1536 * 1024 * 1024)) ;;
        tn|cn1|cn2) printf '%s\n' $((2560 * 1024 * 1024)) ;;
        proxy) printf '%s\n' $((512 * 1024 * 1024)) ;;
        *) return 1 ;;
    esac
}

memory_string_bytes() {
    local value="${1^^}" number suffix
    if [[ "${value}" =~ ^([0-9]+)([KMGTP]?)B?$ ]]; then
        number="${BASH_REMATCH[1]}"; suffix="${BASH_REMATCH[2]}"
        case "${suffix}" in
            K) number=$((number * 1024)) ;;
            M) number=$((number * 1024 * 1024)) ;;
            G) number=$((number * 1024 * 1024 * 1024)) ;;
            T) number=$((number * 1024 * 1024 * 1024 * 1024)) ;;
            P) number=$((number * 1024 * 1024 * 1024 * 1024 * 1024)) ;;
        esac
        printf '%s\n' "${number}"
        return 0
    fi
    return 1
}

cgroup_available() {
    [[ -r /sys/fs/cgroup/cgroup.controllers ]] || return 1
    grep -qw memory /sys/fs/cgroup/cgroup.controllers || return 1
    command -v systemd-run >/dev/null 2>&1 || return 1
    systemctl --user show-environment >/dev/null 2>&1 || return 1
}

preflight_growth_gate() {
    local available required=0 name current cg max
    available="$(mem_available_bytes)" || die "cannot read MemAvailable"
    for name in log tn cn1 cn2 proxy; do
        max="$(memory_max_bytes "${name}")"
        current=0
        cg="${1:-}"
        if [[ -n "${cg}" && -r "/sys/fs/cgroup${cg}/memory.current" ]]; then
            current="$(cat "/sys/fs/cgroup${cg}/memory.current")"
        fi
        (( current < max )) && required=$((required + max - current))
    done
    (( available - required >= 4 * 1024 * 1024 * 1024 )) ||
        die "memory growth gate failed: MemAvailable=${available}, required_growth=${required}, reserve=4GiB"
    printf '%s\n' "${available}:${required}"
}

preflight_manifest_growth_gate() {
    local available required=0 name cg cur max
    available="$(mem_available_bytes)" || die "cannot read MemAvailable"
    for name in log tn cn1 cn2 proxy; do
        unset CGROUP MEMORY_MAX || true
        manifest_load "${name}" 2>/dev/null || continue
        cg="${CGROUP:-}"
        if [[ -n "${cg}" && -r "/sys/fs/cgroup${cg}/memory.max" && -r "/sys/fs/cgroup${cg}/memory.current" ]]; then
            max="$(cat "/sys/fs/cgroup${cg}/memory.max")"
            cur="$(cat "/sys/fs/cgroup${cg}/memory.current")"
            [[ "${max}" =~ ^[0-9]+$ && "${cur}" =~ ^[0-9]+$ ]] || die "invalid cgroup memory values for ${name}"
            (( cur < max )) && required=$((required + max - cur))
        else
            max="$(memory_max_bytes "${name}")"
            required=$((required + max))
        fi
    done
    (( available - required >= 4 * 1024 * 1024 * 1024 )) ||
        die "memory growth gate failed: MemAvailable=${available}, required_growth=${required}, reserve=4GiB"
    printf '%s\n' "${available}:${required}"
}

manifest_write() {
    local name="$1"; shift
    local dst="${MO_25782_RUNTIME}/manifest/${name}.manifest"
    local tmp="${dst}.tmp.$$"
    umask 077
    {
        printf 'name=%q\n' "${name}"
        printf '%s\n' "$@"
    } >"${tmp}"
    chmod 0600 -- "${tmp}"
    mv -f -- "${tmp}" "${dst}"
}

manifest_load() {
    local name="$1"
    local file="${MO_25782_RUNTIME}/manifest/${name}.manifest"
    [[ -f "${file}" && ! -L "${file}" ]] || return 1
    # shellcheck disable=SC1090
    source "${file}"
}

proc_starttime() {
    local pid="$1" field
    [[ -r "/proc/${pid}/stat" ]] || return 1
    field="$(awk '{print $22}' "/proc/${pid}/stat")"
    [[ "${field}" =~ ^[0-9]+$ ]] || return 1
    printf '%s\n' "${field}"
}

proc_identity_matches() {
    local pid="$1" expected_start="$2" expected_exe="$3" expected_cmd="$4"
    [[ -d "/proc/${pid}" ]] || return 1
    [[ "$(proc_starttime "${pid}")" = "${expected_start}" ]] || return 1
    [[ "$(readlink -e -- "/proc/${pid}/exe")" = "${expected_exe}" ]] || return 1
    [[ "$(tr '\0' ' ' <"/proc/${pid}/cmdline")" = "${expected_cmd}" ]] || return 1
}

mysql_exec() {
    local host="$1" port="$2" sql="$3" timeout="${MO_25782_QUERY_TIMEOUT:-5}"
    local mysql_bin="${MO_25782_MYSQL_BIN:-mysql}"
    command -v "${mysql_bin}" >/dev/null 2>&1 || return 127
    if [[ -n "${MO_25782_MYSQL_PASSWORD:-111}" ]]; then
        MYSQL_PWD="${MO_25782_MYSQL_PASSWORD:-111}" timeout "${timeout}s" "${mysql_bin}" --protocol=tcp -h "${host}" -P "${port}" -u "${MO_25782_MYSQL_USER:-root}" \
            --connect-timeout="${timeout}" --batch --raw --skip-column-names --skip-ssl -e "${sql}"
    else
        timeout "${timeout}s" "${mysql_bin}" --protocol=tcp -h "${host}" -P "${port}" -u "${MO_25782_MYSQL_USER:-root}" \
            --connect-timeout="${timeout}" --batch --raw --skip-column-names --skip-ssl -e "${sql}"
    fi
}

mysql_proxy_exec() {
    mysql_exec 127.0.0.1 "${PROXY_PORT:-6001}" "$1"
}

