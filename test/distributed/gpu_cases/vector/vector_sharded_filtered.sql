-- vector_sharded_filtered.sql — pre-filtered search on a SHARDED GPU index
--
-- GPU REQUIRED. Exercises the per-shard USER-FILTER path of SHARDED search
-- under the single-GPU simulation: gpu_multi_simulation presents N logical
-- GPUs (all on physical device 0). Each shard evaluates the pushed-down PK
-- predicate over its OWN row range [shard_offset, shard_offset+shard_sz) and
-- the per-shard top-k is merged. A wrong per-shard offset/slice would return
-- rows the filter should exclude, or miss the true nearest across shards.
--
-- Scope note: this covers the user-filter slice (build_filter_host_mask /
-- upload_host_mask, rank-indexed). The *deletes-only* per-shard bitset cache
-- that was rank-keyed in cgo/cuvs/index_base.hpp (device_shard_bitsets_) is a
-- different path, covered by the Go test
-- pkg/cuvs.TestSimulatedShardedDeleteIvfFlat. Together they cover both halves
-- of filtered SHARDED search under simulation.
--
-- Data: 128 rows, id i -> vecf32(8) of all-i (well separated). 2-way split =
-- 64/64: shard 0 = ids 1..64, shard 1 = ids 65..128. l2_distance(vec_i, vec_k)
-- is monotone in |i-k|, and every probe below has a large unique gap to its
-- nearest surviving id, so the top-1 is deterministic.
-- =====================================================================

