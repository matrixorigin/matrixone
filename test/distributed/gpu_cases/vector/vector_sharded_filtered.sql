-- Filter keeps ONLY shard 1's id range: probing a shard-0 vector (30) must
-- return the nearest surviving id (65) -> shard 0 fully excluded by the filter.
select id from t where id >= 65 order by l2_distance(v, '[30,30,30,30,30,30,30,30]') limit 1;

-- Filter keeps ONLY shard 0's id range: probing a shard-1 vector (100) -> 64.
select id from t where id <= 64 order by l2_distance(v, '[100,100,100,100,100,100,100,100]') limit 1;

-- Filter [50,80] SPANS the shard boundary (50..64 in shard 0, 65..80 in shard 1):
-- both shards apply the predicate and the merge picks the global nearest.
select id from t where id between 50 and 80 order by l2_distance(v, '[30,30,30,30,30,30,30,30]') limit 1;
select id from t where id between 50 and 80 order by l2_distance(v, '[65,65,65,65,65,65,65,65]') limit 1;
select id from t where id between 50 and 80 order by l2_distance(v, '[128,128,128,128,128,128,128,128]') limit 1;

-- Filter excludes everything below 100 (most of both shards): probe 50 -> 100.
select id from t where id >= 100 order by l2_distance(v, '[50,50,50,50,50,50,50,50]') limit 1;

