-- @suit
-- @case
-- @desc:test for bit_count() function
-- @label:bvt

-- Basic tests with constants
SELECT BIT_COUNT(0);
SELECT BIT_COUNT(1);
SELECT BIT_COUNT(2);
SELECT BIT_COUNT(3);
SELECT BIT_COUNT(7);
SELECT BIT_COUNT(15);
SELECT BIT_COUNT(255);
SELECT BIT_COUNT(-1);
SELECT BIT_COUNT(2147483647);
SELECT BIT_COUNT(9223372036854775807);

-- Test with different integer types
DROP TABLE IF EXISTS t;
CREATE TABLE t(
	tiny TINYINT NOT NULL,
	small SMALLINT NOT NULL,
	int_t INT NOT NULL,
	big BIGINT NOT NULL
);
INSERT INTO t VALUES (-128, -32768, -2147483648, -9223372036854775808);
INSERT INTO t VALUES (127, 32767, 2147483647, 9223372036854775807);
INSERT INTO t VALUES (0, 0, 0, 0);
INSERT INTO t VALUES (1, 1, 1, 1);
INSERT INTO t VALUES (255, 65535, 4294967295, 18446744073709551615);

SELECT BIT_COUNT(tiny), BIT_COUNT(small), BIT_COUNT(int_t), BIT_COUNT(big) FROM t;

-- Test with UNSIGNED integer types
DROP TABLE IF EXISTS t;
CREATE TABLE t(
	tiny TINYINT UNSIGNED NOT NULL,
	small SMALLINT UNSIGNED NOT NULL,
	int_t INT UNSIGNED NOT NULL,
	big BIGINT UNSIGNED NOT NULL
);
INSERT INTO t VALUES (0, 0, 0, 0);
INSERT INTO t VALUES (1, 1, 1, 1);
INSERT INTO t VALUES (255, 65535, 4294967295, 18446744073709551615);

SELECT BIT_COUNT(tiny), BIT_COUNT(small), BIT_COUNT(int_t), BIT_COUNT(big) FROM t;

-- Test with expressions
DROP TABLE IF EXISTS t;
CREATE TABLE t(
	a INT NOT NULL,
	b INT NOT NULL
);
INSERT INTO t VALUES (1, 2);
INSERT INTO t VALUES (3, 4);
INSERT INTO t VALUES (7, 8);

SELECT BIT_COUNT(a), BIT_COUNT(b), BIT_COUNT(a + b), BIT_COUNT(a * b) FROM t;
SELECT BIT_COUNT(a | b), BIT_COUNT(a & b), BIT_COUNT(a ^ b) FROM t;

-- Test with NULL values
DROP TABLE IF EXISTS t;
CREATE TABLE t(
	a INT
);
INSERT INTO t VALUES (1);
INSERT INTO t VALUES (NULL);
INSERT INTO t VALUES (255);

SELECT BIT_COUNT(a) FROM t;

-- Test edge cases
SELECT BIT_COUNT(0);
SELECT BIT_COUNT(1);
SELECT BIT_COUNT(255);
SELECT BIT_COUNT(256);
SELECT BIT_COUNT(65535);
SELECT BIT_COUNT(65536);
SELECT BIT_COUNT(4294967295);
SELECT BIT_COUNT(4294967296);
SELECT BIT_COUNT(18446744073709551615);
